function cleanInternalPath(href?: string) {
  if (!href || href.startsWith("#") || href.startsWith("mailto:") || href.startsWith("tel:")) {
    return "/";
  }

  try {
    const url = new URL(href, "https://www.4x4models.com");
    if (!["4x4models.com", "www.4x4models.com"].includes(url.hostname)) return "/";
    const pathname = url.pathname.replace(/\/$/, "") || "/";
    const localized = pathname.startsWith("/en/") ? pathname.slice(3) : pathname === "/en" ? "/" : pathname;
    return `${localized}${url.search}`;
  } catch {
    const [pathPart, queryPart] = href.split("?");
    const pathname = pathPart.replace(/\/$/, "") || "/";
    return `${pathname}${queryPart ? `?${queryPart}` : ""}`;
  }
}

function lastSegment(pathname: string) {
  return pathname.split("?")[0].split("/").filter(Boolean).at(-1) || "home";
}

function queryValue(pathname: string, key: string) {
  const query = pathname.split("?")[1];
  if (!query) return "";

  return new URLSearchParams(query).get(key) || "";
}

function imageFromKeywords(pathname: string) {
  if (/bronco|ford|raptor|pre-runner|sema|truck/.test(pathname)) return brandImages.ford;
  if (/jeep|wrangler|gladiator|rock|badge/.test(pathname)) return brandImages.jeep;
  if (/land-cruiser|toyota|hilux|4runner|fj-cruiser|lc70/.test(pathname)) return brandImages.toyota;
  if (/defender|range-rover|discovery|camel/.test(pathname)) return brandImages["land-rover"];
  if (/g-class|g550|g580|mercedes/.test(pathname)) return brandImages["mercedes-benz"];
  if (/overland|expedition|trail|recovery|tent|camp/.test(pathname)) return collectionImages["beste-4x4-voor-overlanding"];
  if (/snow|ijs|winter|ardennen/.test(pathname)) return collectionImages["beste-4x4-sneeuw-ijs"];
  if (/woestijn|desert|sand|dune|texas/.test(pathname)) return collectionImages["beste-4x4-woestijn"];
  if (/electric|elektrisch|ev/.test(pathname)) return collectionImages["beste-elektrische-4x4-2026"];
  if (/differentieel|locker|lock|awd|4wd|techniek/.test(pathname)) return blogImages["differentieelslot-open-limited-slip-locking"];
  return DEFAULT_MIRROR_IMAGE;
}

export function mirrorImageForPath(href?: string) {
  const pathname = cleanInternalPath(href);
  const slug = lastSegment(pathname);

  if (pathname === "/" || pathname === "/home") return DEFAULT_MIRROR_IMAGE;
  if (pathname.startsWith("/forum?categorie=")) {
    return forumCategoryImages[queryValue(pathname, "categorie")] || imageFromKeywords(pathname);
  }
  if (pathname.startsWith("/shop?categorie=")) {
    return shopCategoryImages[queryValue(pathname, "categorie")] || imageFromKeywords(pathname);
  }
  if (pathname.startsWith("/blog/")) return blogImages[slug] || imageFromKeywords(pathname);
  if (pathname.startsWith("/journal/")) return journalImages[slug] || imageFromKeywords(pathname);
  if (pathname.startsWith("/collecties/")) return collectionImages[slug] || imageFromKeywords(pathname);
  if (pathname.startsWith("/shop/")) return shopImages[slug] || imageFromKeywords(pathname);
  if (pathname.startsWith("/leren/")) return explainerImages[slug] || imageFromKeywords(pathname);
  if (pathname.startsWith("/amerikaans/")) return americaImages[slug] || imageFromKeywords(pathname);
  if (pathname.startsWith("/forum/")) return forumImages[slug] || imageFromKeywords(pathname);

  if (pathname.startsWith("/merken/")) {
    const brand = pathname.split("/").filter(Boolean)[1];
    return brandImages[brand] || imageFromKeywords(pathname);
  }

  if (pathname === "/merken") return brandImages.toyota;
  if (pathname === "/blog") return blogImages["defender-vs-wrangler-vs-land-cruiser"];
  if (pathname === "/journal") return journalImages["defender-octa-2026-update"];
  if (pathname === "/collecties") return collectionImages["beste-4x4-voor-overlanding"];
  if (pathname === "/shop") return shopImages["land-rover-defender-90-camel-trophy-1-18"];
  if (pathname === "/leren") return explainerImages["wat-zijn-4x4-modellen"];
  if (pathname === "/amerikaans") return blogImages["amerikaanse-v8-4x4-ford-raptor-ram-trx"];
  if (pathname === "/forum") return DEFAULT_MIRROR_IMAGE;

  return imageFromKeywords(pathname);
}

export function mirrorCssVarsForHref(href?: string) {
  const image = mirrorImageForPath(href);
  return `--card-photo: url('${image}'); --forum-photo: url('${image}'); --forum-category-photo: url('${image}');`;
}

